""" 
Conversão 
Conversão de Tipos ou Coerção 
é o ato de converter um tipo de caracter em outro tipo. 
Tipos primitivos : Inteiro, Float e String e Boleano. 


"""
#String 
print('1')
print(type('1'))


#String  para Inteiro 
print (int('1'))
print(type(int('1')))

#String  para Float 
print (float('1'))
print(type(float('1')))

#Inteiro para String 
print (str(1))
print(type(str(1)))

#String para Boleano
print (bool('1'))
print(type(bool('1')))