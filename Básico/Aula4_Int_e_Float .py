#Int e Float 

""" 
int - Número inteiro sem casas decimais. 
Representa qualquer número positivo ou negativo. 
Int sem sinal é considerado número positivo . 

"""
print (1,2,3,4)
print (type(13))
print(-3,-2,-1)
print (type(-32))

""" Float 
Float - é um número com casas decimais ou ponto flutuante. 
Pode ser um número positivo ou negativo com casas decimais. 
Float sem sinal é considerado número positivo. 

"""
print (1.2,3.4,5.03)
print (type(.03))
print(-3.2,-1.9)
print (type(-3.2))



""" Função Type 
    Mostra o tipo de valor fornecido. 
"""
print (type(0))
print (type(-3.2))

""" Função Bool ou Boleana
    Valor verdadeiro ou falso de uma sentença. 
"""
print (bool(10>3))
print (bool(""))